package Model;

/**
 *
 * @author Tu
 */
public class Cart {
    
    private int id;
    private Users user;

    public Cart() {
    }

    public Cart(int id, Users user) {
        this.id = id;
        this.user = user;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Users getUser() {
        return user;
    }

    public void setUser(Users user) {
        this.user = user;
    }

    @Override
    public String toString() {
        return "Cart{" + "id=" + id + ", user=" + user + '}';
    }


    
}
